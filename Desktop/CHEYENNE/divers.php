                <p class="paragraphejs" id="paragrapheMygale"> Une morsure de mygale peut être très douloureuse et se
                    prolonger sur plusieurs
                    jours. Son venin, neurotoxique, peut même être mortel. Pourtant, des chercheurs australiens
                    démontrent qu'en isolant une toxine, appelée ProTx-II, son venin peut devenir une alternative aux
