                    Les conclusions de cette surprenante étude ont été présentés par les chercheurs de l'université de
                    Queensland (Australie) lors du 60ème congrès annuel de la société de biophysique, qui s'est tenu à
                    Los Angeles du 27 février au 2 mars 2016. Ils démontrent que la toxine ProTx-II de la mygale
                    (Thrixopelma pruriens), qui se trouve au Pérou, peut cibler certains récepteurs du cerveau et
                    permettre ainsi d'inhiber la sensation de douleur.

                    Le rôle de la membrane des neurones à prendre en compte

                    Ces travaux sont les premiers à décrire le potentiel de la toxine de façon aussi détaillée. "Jusqu'à
                    présent, les études reconnaissaient l'activité inhibitrice des toxines du venin mais elles ont
                    ignoré le rôle potentiel de la membrane cellulaire dans leur activité", explique Sonia Troeira
                    Henriques, co-auteure de l'étude. En clair, la toxine se lie aux récepteurs de la douleur situés
                    dans la membrane des neurones pour agir comme un antidouleur.</p>
